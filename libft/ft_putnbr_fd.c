#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char b;

	if (n == -2147483648)
	{
		write(fd, "-", 1);
		n = n * -1;
		n = n - 2000000000;
		write(fd, "2", 1);
	}
	else if (n < 0)
	{
		write(fd, "-", 1);
		n = n * -1;
	}
	b = n % 10 + 48;
	if (n / 10 >= 1)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	write(fd, &b, 1);
}
